    public function show($id)
    {
        $user = User::find($id);
        $users = User::where('id','>',1)->where('role','>=',10)->get();

        if($user['ban_flg'] === 0) {

            $user->ban_flg = 1;
            $user->save();

            return redirect('admin');

        }else if($user['ban_flg'] === 1) {

            $user->ban_flg = 0;
            $user->save();

            return redirect('admin');
        }
    }

